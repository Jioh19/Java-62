package com.edutecno.vistas;

public abstract class MenuTemplate {
	abstract public void crearProducto();
	abstract public void crearCliente();
	abstract public void crearVenta();
	abstract public void exportarDatos();
	abstract public void importarDatos();
	abstract public void mostrarProductos();
	abstract public void mostrarClientes();
	abstract public void mostrarVentas();
	abstract public void iniciarMenu();
}
